package megamek.common.jacksonadapters;

import com.fasterxml.jackson.databind.ObjectMapper;
import com.fasterxml.jackson.dataformat.yaml.YAMLFactory;
import com.fasterxml.jackson.dataformat.yaml.YAMLGenerator;
import megamek.SuiteConstants;
import megamek.common.BTObject;
import megamek.common.alphaStrike.ASCardDisplayable;
import megamek.common.strategicBattleSystems.SBFFormation;
import megamek.common.strategicBattleSystems.SBFUnit;

import java.io.File;
import java.io.IOException;
import java.io.PrintStream;
import java.time.LocalDate;
import java.util.List;
import java.util.stream.Collectors;

public class MMUWriter {

    static final ObjectMapper conciseMapper =
            new ObjectMapper(new YAMLFactory().disable(YAMLGenerator.Feature.WRITE_DOC_START_MARKER));

    static final ObjectMapper fullStatsMapper =
            new ObjectMapper(new YAMLFactory().disable(YAMLGenerator.Feature.WRITE_DOC_START_MARKER));

    static {
        conciseMapper.setConfig(conciseMapper.getSerializationConfig().withView(Views.CanonCacheLinks.class));
        fullStatsMapper.setConfig(fullStatsMapper.getSerializationConfig().withView(Views.FullStats.class));
    }

    public static void writeMMUFile(File file, List<? extends BTObject> contents) throws IOException {
        write(file, contents, conciseMapper);
    }

    public static void writeMMUFileFullStats(File file, List<? extends BTObject> contents) throws IOException {
        write(file, contents, fullStatsMapper);
    }

    public static void writeMMUFile(File file, BTObject contents) throws IOException {
        writeMMUFile(file, List.of(contents));
    }

    public static void writeMMUFileFullStats(File file, BTObject contents) throws IOException {
        writeMMUFileFullStats(file, List.of(contents));
    }

    private static void write(File file, List<? extends BTObject> contents, ObjectMapper mapper) throws IOException {
        List<BTObject> supportedContents = filteredList(contents);
        PrintStream outputStream = new PrintStream(file);
        outputStream.println("# Generated by " + SuiteConstants.PROJECT_NAME + " "
                + SuiteConstants.VERSION + " on " + LocalDate.now());
        outputStream.println("# This file uses YAML formatting.");
        outputStream.println("# When editing, make sure to keep the indentation and lists intact!");
        if (supportedContents.size() == 1) {
            mapper.writeValue(outputStream, contents.get(0));
        } else {
            mapper.writeValue(outputStream, contents);
        }
    }

    private static boolean isSupported(BTObject content) {
        return content instanceof ASCardDisplayable || content instanceof SBFFormation || content instanceof SBFUnit;
    }

    private static List<BTObject> filteredList(List<? extends BTObject> original) {
        return original.stream().filter(MMUWriter::isSupported).collect(Collectors.toList());
    }

    static class Views {
        static class FullStats { }

        static class CanonCacheLinks { }
    }

    private MMUWriter() { }
}
